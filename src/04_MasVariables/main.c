#include <stdio.h>

int main()
{
    //Definiendo los valores
    int integerA = 1;
    float floatA = 5.5;
    char charA = 'D';
    double doubleA = 1.255;

    //Imprimiendo los valores
    printf("El valor de 'integerA' es: %i \n", integerA);
    printf("El valor de 'floatA' es: %f \n", floatA);
    printf("El valor de 'charA' es: %c \n", charA);
    printf("El valor de 'doubleA' es: %f \n", doubleA);


}
